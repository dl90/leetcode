import { test } from './test.js'
/*
  Given two strings s and t which consist of only lowercase letters.
  String t is generated by random shuffling string s and then add one more letter at a random position.
  Find the letter that was added in t.

  Example:
    Input: s = "abcd", t = "abcde"
    Output: e
    Explanation: 'e' is the letter that was added.
*/

var findTheDifference = function (s, t) {
  const _s = [...s].sort()
  const _t = [...t].sort()

  for (let i = 0; i < t.length; i++) {
    if (_s[i] !== _t[i]) return _t[i]
  }
  return ''
}

function convertToNum (s, t) {
  const sSum = Array.from(s).reduce((acc, cur) => acc + cur.charCodeAt(0), 0)
  const tSum = Array.from(t).reduce((acc, cur) => acc + cur.charCodeAt(0), 0)
  const res = tSum - sSum
  return res > 0 ? String.fromCharCode(tSum - sSum) : ''
}

function oneLoop (s, t) {
  const len = t.length
  if (s.length === len) return ''
  let count = t.charCodeAt(len - 1)

  for (let i = 0; i < len - 1; i++) {
    count ^= s.charCodeAt(i)
    count ^= t.charCodeAt(i)
  }
  return String.fromCharCode(count)
}

test(
  findTheDifference,
  ['abcd', 'abcd'], // ''
  ['abcd', 'abcde'], // e
  ['abcdefghijklmnopqrstuvwsy', 'abcdefghijklzmnopqrstuvwsy'], // z
  ['abcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyzz', 'abcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyzzz'] // z
)

test(
  convertToNum,
  ['abcd', 'abcd'], // ''
  ['abcd', 'abcde'], // e
  ['abcdefghijklmnopqrstuvwsy', 'abcdefghijklzmnopqrstuvwsy'], // z
  ['abcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyzz', 'abcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyzzz'] // z
)

test(
  oneLoop,
  ['abcd', 'abcd'], // ''
  ['abcd', 'abcde'], // e
  ['abcdefghijklmnopqrstuvwsy', 'abcdefghijklzmnopqrstuvwsy'], // z
  ['abcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyzz', 'abcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyabcdefghijklmnopqrstuvwsyzzz'] // z
)
